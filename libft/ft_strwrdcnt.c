int	ft_strwrdcnt(char const *s, char c)
{
	int	i;
	int res;

	i = 0;
	res = 0;
	while (s[i])
	{
		if (s[i] != c)
			res++;
		while (s[i] && s[i] != c)
			i++;
		while (s[i] && s[i] == c)
			i++;
	}
	return (res);
}
