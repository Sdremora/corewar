#include "libft.h"

void	ft_putnstr_fd(char const *s, int n, int fd)
{
	int i;

	if (!s)
		return ;
	i = 0;
	while (*s && i < n)
	{
		ft_putchar_fd(*s, fd);
		s++;
		i++;
	}
}
