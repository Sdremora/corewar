#include "libft.h"

int	ft_arrnumissrt(t_arrnum a)
{
	int i;

	i = 0;
	while (i < a.size - 1)
	{
		if (a.arr[i] > a.arr[i + 1])
			return (0);
		i++;
	}
	return (1);
}
